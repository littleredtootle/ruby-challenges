weather = 'sunny'

case weather
when 'sunny'
  puts "Wear one or two layers and no winter boots!"
when 'cloudy'
  puts "Wear two or three layers and watch out for rain!"
when 'foggy'
  puts "Maybe add another layer or two - a light jumper would be nice."
when 'rainy'
  puts "Wear three layers or so, with an umbrella or waterproof jacket, and wellies."
else
  puts "Well, wear whatever you think is best!"
end
